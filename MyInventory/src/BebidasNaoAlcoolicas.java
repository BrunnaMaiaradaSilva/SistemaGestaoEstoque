
public class BebidasNaoAlcoolicas {

	private String suco;
	private String agua;
	private String refrigerante;
	private String achocolatado;

	public String getSuco() {
		return suco;
	}

	public void setSuco(String suco) {
		this.suco = suco;
	}

	public String getAgua() {
		return agua;
	}

	public void setAgua(String agua) {
		this.agua = agua;
	}

	public String getRefrigerante() {
		return refrigerante;
	}

	public void setRefrigerante(String refrigerante) {
		this.refrigerante = refrigerante;
	}

	public String getAchocolatado() {
		return achocolatado;
	}

	public void setAchocolatado(String achocolatado) {
		this.achocolatado = achocolatado;
	}
}
